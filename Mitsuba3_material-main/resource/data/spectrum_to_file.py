def find_and_group_by_prefix(prefix, locals_dict):
    result = {}
    for name, lst in locals_dict.items():
        if name.startswith(prefix) and isinstance(lst, list):
            result[f"{name}_wavelength"] = np.array(lst[::2], dtype=float)
            result[f"{name}_power"] = np.array(lst[1::2])
    return result

